// Adding two numbers
#include <iostream>
using namespace std;

int main() {
    
    int num_1;
    int num_2;
    
    cout << "Enter a number: ";
    cin >> num_1;
    
    cout << "Enter another number: ";
    cin >> num_2;
    

    cout << "The addition of both the numbers is: " << num_1 + num_2;
    
    return 0;
}
